// The type conversion that you do manually is known as explicit type conversion.

// 1. String to Number ('Number()')
let result;

let a = "3";
console.log(typeof a); //old

result = Number(a);
console.log(typeof result); //new

// 2. Boolean to Number
let b = true;
console.log(typeof b); //old

result = Number(b);
console.log(typeof result); //new

//  >>> Methods used for conversion
// 1. Number()
// 2. String()
// 3. Boolean()
