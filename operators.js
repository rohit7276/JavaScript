console.log("Operators in JavaScript")
let a = 45;
let b = 4;
console.log("a + b =",a+b);
console.log("a - b =",a-b);
console.log("a * b =",a*b);
console.log("a / b =",a/b);
console.log("a ** b =",a**b);
// This is exponential operator, i.e, 45 to the power 4

// *** Increment Operator ****
